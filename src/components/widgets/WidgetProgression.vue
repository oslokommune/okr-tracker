<template>
  <widget :widget-id="widgetId" :title="getTitle()">
    <svg ref="svg"></svg>
  </widget>
</template>

<script>
import { mapState } from 'vuex';
import PieChart from '@/util/PieChart';

export default {
  name: 'WidgetProgression',

  components: {
    Widget: () => import('./Widget.vue'),
  },

  props: {
    widgetId: {
      type: String,
      required: true,
    },
    data: {
      type: Object,
      required: true,
    },
    dimmed: {
      type: Boolean,
      required: false,
      default: false,
    },
    type: {
      type: String,
      required: true,
    },
  },

  data: () => ({
    svg: null,
    chart: null,
  }),

  computed: {
    ...mapState(['theme']),
  },

  watch: {
    data: {
      immediate: true,
      deep: true,
      handler(data) {
        if (!this.chart) return;
        this.chart.render(data, this.theme);
      },
    },

    theme: {
      immediate: true,
      handler() {
        if (!this.chart) return;
        this.chart.render(this.data, this.theme);
      },
    },
  },

  mounted() {
    setTimeout(() => {
      this.svg = this.$refs.svg;
      this.chart = new PieChart(this.svg, { dimmed: this.dimmed, colorMode: this.theme });
      this.chart.render(this.data, this.theme);
    }, 150);
  },

  methods: {
    getTitle() {
      return this.$t(`widget.progression.${this.type}`);
    },
  },
};
</script>
